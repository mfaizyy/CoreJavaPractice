package ForLoop;
/**
 * This program uses java for each loop, it will print elements of an array of collection
 * @author MO605234
 *
 */

public class JavaForEachLoop {
	public static void main(String[] args) {
		int a[]={10,20,30,40,50,60,70,80,9,0,1};
		for(int i:a)
		{
			System.out.println(i);
		}
	}

}
